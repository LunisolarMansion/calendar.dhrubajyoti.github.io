/* =========================================================
   AUTO-GENERATE SUNDAYS + 2ND / 4TH SATURDAYS (2026)
   ========================================================= */

(function () {

  const YEAR = 2026;
  let autoId = 9000;

  for (let month = 0; month < 12; month++) {

    let saturdays = [];

    for (let day = 1; day <= 31; day++) {

      const d = new Date(YEAR, month, day);
      if (d.getMonth() !== month) continue;

      const date =
        `${YEAR}/${String(month + 1).padStart(2, '0')}/${String(day).padStart(2, '0')}`;

      /* ----- SUNDAY ----- */
      if (d.getDay() === 0) {
        window.calendarEvents.push({
          id: 'SUN' + autoId++,
          name: 'Holiday',
          date: date,
          description: 'Sunday',
          type: 'Holiday',
          color: '#3FA9F5'
        });
      }

      /* ----- SATURDAY ----- */
      if (d.getDay() === 6) {
        saturdays.push(date);
      }
    }

    /* ----- 2nd Saturday ----- */
    if (saturdays[1]) {
      window.calendarEvents.push({
        id: 'SAT2' + autoId++,
        name: 'Holiday',
        date: saturdays[1],
        description: '2nd Saturday',
        type: 'Holiday',
        color: '#3FA9F5'
      });
    }

    /* ----- 4th Saturday ----- */
    if (saturdays[3]) {
      window.calendarEvents.push({
        id: 'SAT4' + autoId++,
        name: 'Holiday',
        date: saturdays[3],
        description: '4th Saturday',
        type: 'Holiday',
        color: '#3FA9F5'
      });
    }
  }

})();
